function easeInOutCubic(t: number): number {
  return t < 0.5 ? 4 * t * t * t : 1 - Math.pow(-2 * t + 2, 3) / 2;
}


export function Signature() {
  const pathRefs = useRef<(SVGPathElement | null)[]>([]);

  const rafRef = useRef<number>(0);

  const animate = useCallback(() => {
    const els = pathRefs.current;
    const lengths = els.map((p) => p?.getTotalLength() ?? 0);
    if (lengths.some((l) => l === 0)) return;

    els.forEach((el, i) => {
      if (!el) return;
      el.style.strokeDasharray = `${lengths[i]}`;
      el.style.strokeDashoffset = `${lengths[i]}`;
      el.style.opacity = "0";
    });

    // Timeline phases (seconds):
    // 0.0 - 2.4  : draw on (path 0 starts at 0, path 1 starts at 0.5)
    // 2.4 - 5.0  : hold visible
    // 5.0 - 7.0  : erase (reverse)
    // 7.0 - 9.5  : hold hidden
    const DRAW_DUR = 2.4;
    const PATH_STAGGER = 0.5;
    const HOLD_VISIBLE = 2.6;
    const ERASE_DUR = 1.8;
    const HOLD_HIDDEN = 2.5;
    const TOTAL = DRAW_DUR + HOLD_VISIBLE + ERASE_DUR + HOLD_HIDDEN;

    let start: number | null = null;

    const tick = (ts: number) => {
      if (!start) start = ts;
      const elapsed = ((ts - start) / 1000) % TOTAL;

      els.forEach((el, i) => {
        if (!el) return;
        const len = lengths[i];
        const stagger = i * PATH_STAGGER;

        if (elapsed < DRAW_DUR) {
          // Draw phase
          const localT = Math.max(0, Math.min(1, (elapsed - stagger) / (DRAW_DUR - stagger)));
          const eased = easeInOutCubic(localT);
          el.style.strokeDashoffset = `${len * (1 - eased)}`;
          el.style.opacity = localT > 0 ? "1" : "0";
        } else if (elapsed < DRAW_DUR + HOLD_VISIBLE) {
          // Hold visible
          el.style.strokeDashoffset = "0";
          el.style.opacity = "1";
        } else if (elapsed < DRAW_DUR + HOLD_VISIBLE + ERASE_DUR) {
          // Erase phase (reverse order - path 1 erases first)
          const eraseStart = DRAW_DUR + HOLD_VISIBLE;
          const reverseStagger = (els.length - 1 - i) * PATH_STAGGER * 0.6;
          const localT = Math.max(
            0,
            Math.min(1, (elapsed - eraseStart - reverseStagger) / (ERASE_DUR - reverseStagger * 1.2))
          );
          const eased = easeInOutCubic(localT);
          el.style.strokeDashoffset = `${len * eased}`;
          el.style.opacity = localT < 1 ? "1" : "0";
        } else {
          // Hold hidden
          el.style.strokeDashoffset = `${len}`;
          el.style.opacity = "0";
        }
      });


      rafRef.current = requestAnimationFrame(tick);
    };

    rafRef.current = requestAnimationFrame(tick);
  }, []);

  useEffect(() => {
    // Small delay to ensure paths are rendered and measurable
    const timer = setTimeout(animate, 100);
    return () => {
      clearTimeout(timer);
      cancelAnimationFrame(rafRef.current);
    };
  }, [animate]);

  return (
    <>
    <style>{`
      .signature-fixed {
        position: fixed;
        bottom: 28px;
        left: 50%;
        transform: translateX(-50%);
        z-index: 50;
        pointer-events: none;
      }
      @media (max-height: 700px) {
        .signature-fixed {
          display: none;
        }
      }
      @media (max-width: 639px) {
        .signature-fixed {
          position: static;
          transform: none;
          display: flex;
          justify-content: center;
          padding: 24px 0 40px;
        }
      }
    `}</style>
    <div className="signature-fixed">
      <svg
        width="120"
        height={Math.round(120 * (SVG_HEIGHT / SVG_WIDTH))}
        viewBox={VIEWBOX}
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        style={{ overflow: "visible" }}
      >
        {paths.map((d, i) => (
          <path
            key={i}
            ref={(el) => {
              pathRefs.current[i] = el;
            }}
            d={d}
            stroke="rgba(0,0,0,0.10)"
            strokeWidth="10"
            strokeLinecap="round"
            strokeLinejoin="round"
            fill="none"
            style={{ opacity: 0 }}
          />
        ))}
      </svg>
    </div>
    </>
  );
}
